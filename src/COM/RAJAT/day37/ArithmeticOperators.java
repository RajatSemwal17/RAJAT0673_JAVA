package COM.RAJAT.day37;


public class ArithmeticOperators {
    public static void main(String [] args){
        int a = 8, b = 4;
        System.out.print("Addition Operator (+): ");
        System.out.println(a + b);
        System.out.print("Subtraction Operator(-): ");
        System.out.println(a - b);
        System.out.print("Multiplication Operator(*): ");
        System.out.println(a * b);
        System.out.print("Division Operator(/): ");
        System.out.println(a / b);
        System.out.print("Modulus Operator(%): ");
        System.out.println(a % b);
    }
}